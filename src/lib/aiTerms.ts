/** 日付をシードに、その日表示する3語のインデックスを決める（被りなし） */
function getTodaysIndices(): [number, number, number] {
  const now = new Date();
  const jst = new Date(now.getTime() + 9 * 60 * 60 * 1000);
  const dayOfYear = Math.floor(
    (jst.getTime() - new Date(jst.getUTCFullYear(), 0, 0).getTime()) / 86400000
  );
  const n = TERMS.length;
  if (n < 3) return [0, 0, 0];
  const i0 = Math.abs(dayOfYear) % n;
  const i1 = Math.abs(dayOfYear + 31) % n;
  const i2 = Math.abs(dayOfYear + 67) % n;
  if (i0 !== i1 && i1 !== i2 && i2 !== i0) return [i0, i1, i2];
  return [i0, (i0 + 1) % n, (i0 + 2) % n];
}

/** 日付に応じて1日3語を返す（営業で使いやすいAI用語） */
export function getTodaysAiTerm(): AiTermEntry {
  const [i] = getTodaysIndices();
  const item = TERMS[i];
  return item ?? { term: "—", explanation: "—", talkExample: "—", businessUse: "—" };
}

/** 日付に応じて1日3語を返す（配列） */
export function getTodaysAiTerms(): AiTermEntry[] {
  if (!TERMS.length) return [];
  const [i0, i1, i2] = getTodaysIndices();
  return [TERMS[i0], TERMS[i1], TERMS[i2]].filter(Boolean);
}
