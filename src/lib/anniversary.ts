function getKey(month: number, day: number): string {
  return `${month}-${day}`;
}

/**
 * 今日の記念日を取得。曜日は一切返さない。3行以上の説明を返す。
 */
export function getTodaysAnniversary(overrideDate?: Date): AnniversaryResult {
  const now = overrideDate ?? new Date();
  const jst = new Date(now.getTime() + 9 * 60 * 60 * 1000);
  const month = jst.getUTCMonth() + 1;
  const day = jst.getUTCDate();
  const key = getKey(month, day);
  const entry = ANNIVERSARY_DATA[key];
  if (entry) {
    return { name: entry.name, detail: entry.detail, source: SOURCE };
  }
  const fallbackDetail =
    `この日も日本では様々な記念日・行事が制定・登録されています。\n` +
    `日本記念日協会のデータベースやWikipedia「${month}月${day}日」で検索すると、その日の出来事や記念日を調べられます。\n` +
    `日めくりで楽しんでみてください。`;
  return {
    name: `${month}月${day}日`,
    detail: fallbackDetail,
    source: SOURCE,
  };
}
