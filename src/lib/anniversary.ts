function pad2(n: number): string {
  return String(n).padStart(2, "0");
}

/**
 * 今日の記念日を取得。曜日は一切返さない。
 */
export function getTodaysAnniversary(overrideDate?: Date): AnniversaryResult {
  const d = overrideDate ?? new Date();
  const jst = new Date(d.getTime() + 9 * 60 * 60 * 1000);
  const month = jst.getUTCMonth() + 1;
  const day = jst.getUTCDate();
  const key = `${pad2(month)}-${pad2(day)}`;
  const entry = anniversaries[key];
  if (entry) {
    return { name: entry.title, detail: entry.detail, source: SOURCE };
  }
  const fallbackDetail =
    `この日も日本では様々な記念日・行事が制定・登録されています。\n` +
    `日本記念日協会のデータベースやWikipedia「${month}月${day}日」で検索すると、その日の出来事や記念日を調べられます。\n` +
    `日めくりで楽しんでみてください。`;
  return {
    name: `${month}月${day}日`,
    detail: fallbackDetail,
    source: SOURCE,
  };
}
