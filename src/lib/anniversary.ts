function build365(): Record<string, { title: string; detail: string }> {
  const daysPerMonth = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
  const out: Record<string, { title: string; detail: string }> = { ...anniversaries };
  for (let m = 1; m <= 12; m++) {
    const max = daysPerMonth[m - 1];
    for (let d = 1; d <= max; d++) {
      const key = `${pad2(m)}-${pad2(d)}`;
      if (!out[key]) out[key] = { title: `${m}月${d}日`, detail: FALLBACK_ENTRY.detail };
    }
  }
  return out;
}

const anniversaries365 = build365();

